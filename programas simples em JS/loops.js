
//Estrutura de Repetição usando o "WHILE".

var cont = 0;
while (cont <= 10){
    console.log (cont);
    cont++;
}
 
//Estrutura de repetição Do WHILE.

var c = 0;
do{
    console.log(c);
    c++

}while (c <= 10);

// Estrutura de Repetição usando 'FOR'.

for(var con = 0;con <= 10; con++){
    console.log(con);
}